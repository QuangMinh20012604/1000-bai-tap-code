#define MAX 100

int main()
{
	int N, a[MAX];

	//So phan tu mang
	do
	{
		printf("\nNhap so phan tu cua mang: ");
		scanf_s("%d", &N);
		if(N < 1 || N > MAX)
			printf("\nSo phan tu khong hop le. Xin kiem tra lai !");
	}while(N < 1 || N > MAX);
	
	//Gán phan tu mang
	for(int i = 0; i < N; i++)
	{
		printf("Nhap a[%d]: ", i);
		scanf_s("%d", &a[i]);
	}

	//In mang
	printf("\n");
	for (int i = 0; i < N; i++)
	{
		printf("%d  ", a[i]);
	}


	printf("\nGia tri doi xung trong mang la:\n");
	
	//Xu ly de
	int demdoixung = 0, doixung;

	for(int i = 0; i < N; i++)
	{
		doixung = 0;

		for(int temp = a[i];temp != 0; temp /= 10)
		{
			doixung = doixung * 10 + temp % 10;
		}

		if(doixung == a[i])
		{
			demdoixung++;
			printf("%d  ", a[i]);
		}
	}

	printf("\n\nSo luong gia tri doi xung trong mang la: %d.", demdoixung);

	_getch();
	return 0;
}
